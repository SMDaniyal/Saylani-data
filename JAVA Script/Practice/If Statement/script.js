var input=prompt("Enter your Percentage","Your Percentage");
if (input>=90){
    document.write("A1 Grade")

}
else if(input>=80){
document.write("A Grade")
}
else if(input>=70){
    document.write("B Grade")
}
else if(input>=60){
    document.write("C Grade")
}
else if(input>=50){
    document.write("D Grade")
}
else if(input<50){
    document.write("You are Fail!")
}